SEPARATORS = ['｀', '［', '］', '、', '＝', '‘', '；', '。', '｜', '？', '》', 
              '《', '：', '“', '｛', '｝', '）', '（', '＊', '…', '＃', '！',
              '～', '’', '”', '〕', '〈', '〉', '「', '」', '『', '』', '〖', '〗', 
              '【', '】', '＜', '＞', '`', '~', '!', '@', '#', '^', 
              '&', '*', '\\', '(', ')', '=', '{', '}', '[', ']', 
              '|', ';', ':', "'", '<', '>', '?', "\n", "\t", "\r", 
              ' ', '-', '/', '+', ',', '　']

class Symbol
  def self.filter(char)
    SEPARATORS.include?(char) ? :symbol : char
  end
end